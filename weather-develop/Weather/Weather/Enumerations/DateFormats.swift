import Foundation

//MARK: - DateFormats -
enum DateFormats: String {
    case daily = "EEEE"
    case hourly = "HH"
    case info = "yyyy-MM-dd HH:mm:ss"
    case hourlyFull = "HH.mm"
    case minute = "mm"
    case monthDay = "MMM, d"
}

